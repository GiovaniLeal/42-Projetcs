#include "libft.h"

/* ************************************************************************** */
/* Writes the string 's' to the given file descriptor 'fd'                    */
/* ************************************************************************** */
void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}
