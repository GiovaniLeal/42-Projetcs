#include "libft.h"

/* ************************************************************************** */
/* Writes the string 's' followed by a new line to the given file descriptor  */
/* ************************************************************************** */
void	ft_putendl_fd(char *s, int fd)
{
	int		i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
