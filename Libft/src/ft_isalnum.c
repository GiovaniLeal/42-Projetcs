#include "libft.h"

/* ************************************************************************** */
/* IS_ALPHA_NUMERIC  - Returns 1 is is a digit or a character alse, returns 0 */
/* ************************************************************************** */
int	ft_isalnum(int c)
{
	return (ft_isalpha(c) || ft_isdigit(c));
}
