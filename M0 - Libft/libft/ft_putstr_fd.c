#include "libft.h"

/* ************************************************************************** */
/* Writes the string 's' to the given file descriptor 'fd'                    */
/* ************************************************************************** */
void	ft_putstr_fd(char *s, int fd)
{
	int	index;

	index = 0;
	while (s[index])
	{
		write(fd, &s[index], 1);
		index++;
	}
}
