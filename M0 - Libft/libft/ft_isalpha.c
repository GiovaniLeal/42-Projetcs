/* ************************************************************************** */
// IS_ALPHA - Return 1 if is true or 0 for false.
/* ************************************************************************** */
int	ft_isalpha(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}
